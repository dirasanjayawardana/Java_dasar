public class OperasiMatematika {
    public static void main(String[] args){
        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        // operasi augmented assignments (operasi yang dilakukan kevariabel tersebut sendiri)
        System.out.println(a += 10);
    }
}
